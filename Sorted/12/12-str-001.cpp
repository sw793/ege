#include <bits/stdc++.h>
using namespace std;
int main(){
	string s = "";
	for (int i = 0; i < 61; i++)
		s = s + "7";
	while ( (s.find("777") != -1) || (s.find("111") != -1)){
		if (s.find("111") != - 1){
			int poz = s.find("111");
			s.replace(poz, 3, "7");
		}
		else{
			int poz = s.find("777");
			s.replace(poz, 3, "1");
		}
	}
	cout << s;
}

