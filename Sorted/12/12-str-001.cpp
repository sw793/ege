//Какая строка получится в результате применения приведённой ниже программы к строке, состоящей из 61 идущих подряд цифр 7? В ответе запишите полученную строку.

//НАЧАЛО
//ПОКА нашлось (111) ИЛИ нашлось (777)
  //ЕСЛИ нашлось (111)
    //ТО заменить (111, 7)
